    private void funremoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_funremoverActionPerformed
        int i = JOptionPane.showConfirmDialog(null, "Tens sertesa que queres deletar", "Deletar", JOptionPane.OK_CANCEL_OPTION);
        if (i == JOptionPane.OK_OPTION) {
            Funcionario_Impl fd = new Funcionario_Impl();
            fd.remover(funtabela);
            fd.pesquisar(funpesquisa.getText(), funtabela);
            funrestrito.setVisible(true);
            funome.setText("");
            funsenha.setText("");
            funusuario.setText("");
            fundata.setDate(null);
            funzona.setText("");
            funsalario.setText("");
            funtelemovel.setText("");
            funsalvar.setVisible(true);
        }
    }//GEN-LAST:event_funremoverActionPerformed

