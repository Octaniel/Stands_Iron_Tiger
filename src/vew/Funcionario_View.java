    private void funsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_funsalvarActionPerformed
        Funcionario_Impl fd = new Funcionario_Impl();
        Funcionario f = new Funcionario();
        f.setNome(funome.getText());
        f.setData_de_nascimento(fundata.getDate());
        f.setDistrito(fundistrito.getSelectedItem().toString());
        f.setGenero(fungenero.getSelectedItem().toString());
        f.setZona(funzona.getText());
        f.setSalario(Double.parseDouble(funsalario.getText()));
        f.setTelefone(Long.parseLong(funtelemovel.getText()));
        f.setStatus(funstatus.getSelectedItem().toString());
        f.setSenha(new String(funsenha.getPassword()));
        f.setNome_de_usuario(funusuario.getText());
        fd.adicionar(f);
        fd.pesquisar(funpesquisa.getText(), funtabela);
        funome.setText("");
        funsenha.setText("");
        funusuario.setText("");
        fundata.setDate(null);
        funzona.setText("");
        funsalario.setText("");
        funtelemovel.setText("");
    }//GEN-LAST:event_funsalvarActionPerformed

