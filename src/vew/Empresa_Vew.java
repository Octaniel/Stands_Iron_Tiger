    private void empsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_empsaveActionPerformed
        if (!empnfuncionarios.getText().equals("") && !empnome.getText().equals("") && !empzona.getText().equals("")) {
            Empresa_Impl ed = new Empresa_Impl();
            Empresa e = new Empresa();
            e.setNome(empnome.getText());
            e.setZona(empzona.getText());
            e.setDistrito(empdistrito.getSelectedItem().toString());
            e.setNfuncionarios(Integer.parseInt(empnfuncionarios.getText()));
            ed.inserir(e);
            empnome.setText("");
            empzona.setText("");
            empnfuncionarios.setText("");
            ed.pesquisar(emppesquisa.getText(), emptabela);
        } else {
            JOptionPane.showMessageDialog(null, "Por favor preenche os espaços em branco");
        }
    }//GEN-LAST:event_empsaveActionPerformed

