    private void empremoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_empremoveActionPerformed
        int i = JOptionPane.showConfirmDialog(null, "Tens sertesa que queres deletar", "Deletar", JOptionPane.OK_CANCEL_OPTION);
        if (i == JOptionPane.OK_OPTION) {
            Empresa_Impl ed = new Empresa_Impl();
            ed.remover(emptabela);
            ed.pesquisar(emppesquisa.getText(), emptabela);
            empnome.setText("");
            empzona.setText("");
            empnfuncionarios.setText("");
            empsave.setVisible(true);
        }
    }//GEN-LAST:event_empremoveActionPerformed

    private void empeditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_empeditActionPerformed
        Empresa em = new Empresa();
        Empresa_Impl ed = new Empresa_Impl();
        em.setNome(empnome.getText());
        em.setZona(empzona.getText());
        em.setDistrito(empdistrito.getSelectedItem().toString());
        em.setNfuncionarios(Integer.parseInt(empnfuncionarios.getText()));
        ed.editar(em,emptabela);
        ed.pesquisar(emppesquisa.getText(), emptabela);
        empnome.setText("");
        empzona.setText("");
        empnfuncionarios.setText("");
        empsave.setVisible(true);

    }//GEN-LAST:event_empeditActionPerformed

